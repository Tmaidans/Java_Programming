package dailyClasses.day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");

        System.out.println("------------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("------------------------------------------");

        System.out.println("\\");

        System.out.println("------------------------------------------");

        System.out.println("My favorite TV show is \"Doctor Who\"");
    }

}

/*
    1. \n: New Line
    2. \t: Paragraph Space
    3. \\: Backslash symbol
    4. \": Double quote symbol
 */