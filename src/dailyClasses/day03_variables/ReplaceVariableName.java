package dailyClasses.day03_variables;

public class ReplaceVariableName {

    public static void main(String[] args) {

        String pupil = "Tim";

        System.out.println(pupil);
        System.out.println("pupil = " + pupil);
        System.out.println(pupil);
        System.out.println(pupil);
        System.out.println(pupil);

        int salary = 100_000;

        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);
        System.out.println(salary);


    }
}
